package com.cumaliatalan.myapplication

class SuperKahraman(var isim: String, var yas: Int, var meslek: String) {

    fun setIsim(){
        this.isim = isim
    }

    fun getIsimm() : String{
        return this.isim
    }

    fun setYas(){
        this.yas = yas
    }


    fun getYass() : Int {
        return this.yas
    }

    fun setMeslek(){
        this.meslek = meslek
    }

    fun getMeslekk() : String {
        return this.meslek
    }
}